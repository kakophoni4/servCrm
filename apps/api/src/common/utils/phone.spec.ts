import { normalizePhone } from './phone';

describe('normalizePhone', () => {
  it('заменяет 11-значный номер с ведущей 8 на 7', () => {
    expect(normalizePhone('89161234567')).toBe('79161234567');
  });

  it('добавляет 7 к 10-значному номеру без кода страны', () => {
    expect(normalizePhone('9161234567')).toBe('79161234567');
  });

  it('убирает форматирование и оставляет 11 цифр с 7', () => {
    expect(normalizePhone('+7 (916) 123-45-67')).toBe('79161234567');
  });

  it('оставляет только цифры, отбрасывая буквы и мусор', () => {
    expect(normalizePhone('abc123')).toBe('123');
  });

  it('не меняет короткий номер (<10 цифр) — orders.create вернёт 400', () => {
    expect(normalizePhone('12345')).toBe('12345');
  });

  // РИСК: 11 цифр с ведущей 7 не нормализуется повторно — тот же клиент,
  // что и при вводе 10 цифр без кода, но дубль в БД возможен при разных форматах ввода.
  it('возвращает 11-значный номер с 7 без изменений', () => {
    expect(normalizePhone('79161234567')).toBe('79161234567');
  });

  // РИСК: иностранный/некорректный префикс (не 8, не 10 цифр) проходит как есть —
  // возможны дубли клиентов, если тот же номер введут в другом формате.
  it('возвращает 11-значный номер не с 8 без изменений', () => {
    expect(normalizePhone('99161234567')).toBe('99161234567');
  });
});
